CREATE TABLE pokemon_tipo(
	NUMERO_POKEDEX INT UNSIGNED NOT NULL,
    ID_TIPO INT UNSIGNED NOT NULL,
    CONSTRAINT NUMERO_POKEDEX_FK FOREIGN KEY (NUMERO_POKEDEX) REFERENCES pokemon(NUMERO_POKEDEX)
