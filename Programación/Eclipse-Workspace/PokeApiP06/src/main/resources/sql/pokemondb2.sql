DROP DATABASE IF EXISTS pokemondb2;
CREATE DATABASE pokemondb2;
USE pokemondb2;

CREATE TABLE pokemon(
	NUMERO_POKEDEX INT UNSIGNED AUTO_INCREMENT NOT NULL,
    NOMBRE VARCHAR(15),
    PESO FLOAT(6.3),
    ALTURA FLOAT(6.3),
    DESCRIPCION VARCHAR (512),
    URL VARCHAR (512),
    CONSTRAINT POKEMON_PK PRIMARY KEY (NUMERO_POKEDEX)
);

ALTER TABLE pokemon AUTO_INCREMENT = 1;

/*CREACION DE NUEVA TABLA PARTE 2 - ESTADISTICAS BASE*/
CREATE TABLE estadisticas_base(
	NUMERO_POKEDEX INT UNSIGNED NOT NULL,
    PS INT UNSIGNED NOT NULL,
    ATAQUE INT UNSIGNED NOT NULL,
    DEFENSA INT UNSIGNED NOT NULL,
    ESPECIAL INT UNSIGNED NOT NULL,
    VELOCIDAD INT UNSIGNED NOT NULL,
    CONSTRAINT EB_NUMERO_POKEDEX FOREIGN KEY (NUMERO_POKEDEX) REFERENCES pokemon(NUMERO_POKEDEX)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
	CONSTRAINT ESTADISTICAS_BASE_PK PRIMARY KEY (NUMERO_POKEDEX)
);
/*CREACION DE NUEVA TABLA PARTE 3 - evoluciona_de*/
CREATE TABLE evoluciona_de(
	POKEMON_EVOLUCIONADO INT UNSIGNED NOT NULL,
    POKEMON_ORIGEN INT UNSIGNED NOT NULL,
    CONSTRAINT ED_POKEMON_EVOLUCIONADO_FK FOREIGN KEY (POKEMON_EVOLUCIONADO) REFERENCES pokemon(NUMERO_POKEDEX)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT ED_POKEMON_ORIGEN_FK FOREIGN KEY (POKEMON_ORIGEN) REFERENCES pokemon(NUMERO_POKEDEX)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT ED_PK PRIMARY KEY (POKEMON_EVOLUCIONADO, POKEMON_ORIGEN)
);
/*CREACION DE NUEVA TABLA PARTE 3 - tipo_evolucion*/
CREATE TABLE tipo_evolucion(
	ID_TIPO_EVOLUCION INT UNSIGNED NOT NULL,
    TIPO_EVOLUCION VARCHAR(15) NOT NULL,
    CONSTRAINT TE_PK PRIMARY KEY (ID_TIPO_EVOLUCION)
);
/*CREACION DE NUEVA TABLA PARTE 3 - forma_evolucion*/
CREATE TABLE forma_evolucion(
	ID_FORMA_EVOLUCION INT UNSIGNED NOT NULL,
    TIPO_EVOLUCION  INT UNSIGNED NOT NULL NOT NULL,
    CONSTRAINT FE_TIPO_EVOLUCION_FK  FOREIGN KEY (TIPO_EVOLUCION) REFERENCES tipo_evolucion(ID_TIPO_EVOLUCION)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT FE_PK PRIMARY KEY (ID_FORMA_EVOLUCION)
);
/*CREACION DE NUEVA TABLA PARTE 3 - forma_evolucion*/
CREATE TABLE pokemon_forma_evolucion(
	NUMERO_POKEDEX INT UNSIGNED NOT NULL,
    ID_FORMA_EVOLUCION INT UNSIGNED NOT NULL,
    CONSTRAINT PFE_NUMERO_POKEDEX_FK FOREIGN KEY (NUMERO_POKEDEX) REFERENCES pokemon(NUMERO_POKEDEX)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT PFE_ID_FORMA_EVOLUCION_FK FOREIGN KEY (ID_FORMA_EVOLUCION) REFERENCES forma_evolucion(ID_FORMA_EVOLUCION)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT PFE_PK PRIMARY KEY (NUMERO_POKEDEX, ID_FORMA_EVOLUCION)
);
/*CREACION DE NUEVA TABLA PARTE 3 - nivel_evolucion*/
CREATE TABLE nivel_evolucion(
	ID_FORMA_EVOLUCION INT UNSIGNED NOT NULL,
    NIVEL INT UNSIGNED NOT NULL,
    CONSTRAINT NE_ID_FORMA_EVOLUCION_FK FOREIGN KEY (ID_FORMA_EVOLUCION) REFERENCES forma_evolucion(ID_FORMA_EVOLUCION)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
	CONSTRAINT NE_PK PRIMARY KEY (ID_FORMA_EVOLUCION)
);
/*CREACION DE NUEVA TABLA PARTE 3 - tipo_piedra*/
CREATE TABLE tipo_piedra(
	ID_TIPO_PIEDRA INT UNSIGNED NOT NULL,
    NOMBRE_PIEDRA VARCHAR(20) NOT NULL,
	CONSTRAINT TP_PK PRIMARY KEY (ID_TIPO_PIEDRA)
);
/*CREACION DE NUEVA TABLA PARTE 3 - piedra*/
CREATE TABLE piedra(
	ID_FORMA_EVOLUCION INT UNSIGNED NOT NULL,
    ID_TIPO_PIEDRA INT UNSIGNED NOT NULL,
    CONSTRAINT P_ID_FORMA_EVOLUCION_FK FOREIGN KEY (ID_FORMA_EVOLUCION) REFERENCES forma_evolucion(ID_FORMA_EVOLUCION)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT P_ID_TIPO_PIEDRA_FK FOREIGN KEY (ID_TIPO_PIEDRA) REFERENCES tipo_piedra( ID_TIPO_PIEDRA)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT P_PK PRIMARY KEY (ID_FORMA_EVOLUCION)
);
CREATE TABLE tipo_ataque(
	ID_TIPO_ATAQUE INT UNSIGNED NOT NULL,
    TIPO VARCHAR(8),
    CONSTRAINT TIPO_ATAQUE_PK PRIMARY KEY (ID_TIPO_ATAQUE)
);
CREATE TABLE tipo(
	ID_TIPO INT UNSIGNED NOT NULL,
    NOMBRE VARCHAR(10) NOT NULL,
    ID_TIPO_ATAQUE INT UNSIGNED NOT NULL,
    CONSTRAINT ID_TIPO_ATAQUE_FK FOREIGN KEY (ID_TIPO_ATAQUE) REFERENCES tipo_ataque(ID_TIPO_ATAQUE)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT TIPO_PK PRIMARY KEY (ID_TIPO)
);
CREATE TABLE movimiento(
	ID_MOVIMIENTO INT UNSIGNED NOT NULL,
    NOMBRE VARCHAR(20) NOT NULL,
    POTENCIA INT UNSIGNED NOT NULL,
    PRECISION_MOV INT UNSIGNED NOT NULL,
    DESCRIPCION VARCHAR(500) NOT NULL,
    PP INT UNSIGNED NOT NULL,
    ID_TIPO INT UNSIGNED NOT NULL,
    PRIORIDAD INT  NOT NULL,
	CONSTRAINT ID_TIPO_MOVIMIENTO_FK FOREIGN KEY (ID_TIPO) REFERENCES tipo(ID_TIPO)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT MOVIMIENTO_PK PRIMARY KEY (ID_MOVIMIENTO)
);
/*CREACION DE NUEVA TABLA PARTE 3 - TIPO APRENDIZAJE*/
CREATE TABLE tipo_forma_aprendizaje(
	ID_TIPO_APRENDIZAJE INT UNSIGNED NOT NULL,
    TIPO_APRENDIZAJE VARCHAR (20) NOT NULL,
    CONSTRAINT TFA_PK PRIMARY KEY (ID_TIPO_APRENDIZAJE)
);
/*CREACION DE NUEVA TABLA PARTE 3 - FORMA APRENDIZAJE*/
CREATE TABLE forma_aprendizaje(
	ID_FORMA_APRENDIZAJE INT UNSIGNED NOT NULL,
    ID_TIPO_APRENDIZAJE INT UNSIGNED NOT NULL,
	CONSTRAINT FA_ID_TIPO_APRENDIZAJE_FK FOREIGN KEY (ID_TIPO_APRENDIZAJE) REFERENCES tipo_forma_aprendizaje(ID_TIPO_APRENDIZAJE)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
	CONSTRAINT FORMA_APRENDIZAJE_PK PRIMARY KEY (ID_FORMA_APRENDIZAJE)
);
/*CREACION DE NUEVA TABLA PARTE 3 -  NIVEL APRENDIZAJE*/
CREATE TABLE nivel_aprendizaje (
	ID_FORMA_APRENDIZAJE INT UNSIGNED NOT NULL,
    NIVEL INT UNSIGNED NOT NULL,
    CONSTRAINT NA_FK FOREIGN KEY (ID_FORMA_APRENDIZAJE) REFERENCES forma_aprendizaje(ID_FORMA_APRENDIZAJE),
    CONSTRAINT NA_PK PRIMARY KEY (ID_FORMA_APRENDIZAJE)
);
/*CREACION DE NUEVA TABLA PARTE 3 -  MO*/
CREATE TABLE mo (
	ID_FORMA_APRENDIZAJE INT UNSIGNED NOT NULL,
    MO VARCHAR(5) NOT NULL,
    CONSTRAINT MO_FK FOREIGN KEY (ID_FORMA_APRENDIZAJE) REFERENCES forma_aprendizaje(ID_FORMA_APRENDIZAJE),
    CONSTRAINT MO_PK PRIMARY KEY (ID_FORMA_APRENDIZAJE)
);
/*CREACION DE NUEVA TABLA PARTE 3 -  MT*/
CREATE TABLE mt (
	ID_FORMA_APRENDIZAJE INT UNSIGNED NOT NULL,
    MT VARCHAR(5) NOT NULL,
    CONSTRAINT MT_FK FOREIGN KEY (ID_FORMA_APRENDIZAJE) REFERENCES forma_aprendizaje(ID_FORMA_APRENDIZAJE),
    CONSTRAINT MT_PK PRIMARY KEY (ID_FORMA_APRENDIZAJE)
);
/*CREACION DE NUEVA TABLA PARTE 2 - FORMA MOVIMIENTO*/
CREATE TABLE pokemon_movimiento_forma(
	NUMERO_POKEDEX INT UNSIGNED NOT NULL,
    ID_MOVIMIENTO INT UNSIGNED NOT NULL,
    ID_FORMA_APRENDIZAJE INT UNSIGNED NOT NULL,
    CONSTRAINT MF_NUMERO_POKEDEX_FK FOREIGN KEY (NUMERO_POKEDEX) REFERENCES pokemon(NUMERO_POKEDEX)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT MF_ID_MOVIMIENTO_FK FOREIGN KEY (ID_MOVIMIENTO) REFERENCES movimiento(ID_MOVIMIENTO)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
	CONSTRAINT MF_ID_FORMA_APRENDIZAJE_FK FOREIGN KEY (ID_FORMA_APRENDIZAJE) REFERENCES forma_aprendizaje(ID_FORMA_APRENDIZAJE)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT POKEMON_MOVIMIENTO_FORMA_PK PRIMARY KEY (NUMERO_POKEDEX, ID_MOVIMIENTO, ID_FORMA_APRENDIZAJE)
);


/*CREACION DE NUEVA TABLA PARTE 2 -  EFECTO SECUNDARIO*/

CREATE TABLE efecto_secundario(
	ID_EFECTO_SECUNDARIO INT UNSIGNED NOT NULL,
    EFECTO_SECUNDARIO VARCHAR(30),
    CONSTRAINT ES_PK PRIMARY KEY (ID_EFECTO_SECUNDARIO)
);
/*CREACION DE NUEVA TABLA PARTE 2 - MOVIMIENTO EFECTO SECUNDARIO*/
CREATE TABLE movimiento_efecto_secundario (
	ID_MOVIMIENTO INT UNSIGNED NOT NULL,
    ID_EFECTO_SECUNDARIO INT UNSIGNED NOT NULL,
    PROBABILIDAD FLOAT(6,3),
	CONSTRAINT MES_ID_MOVIMIENTO_FK FOREIGN KEY (ID_MOVIMIENTO) REFERENCES movimiento(ID_MOVIMIENTO)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT MES_ID_EFECTO_SECUNDARIO_FK FOREIGN KEY (ID_EFECTO_SECUNDARIO) REFERENCES efecto_secundario(ID_EFECTO_SECUNDARIO)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
	CONSTRAINT MES_PK PRIMARY KEY (ID_MOVIMIENTO, ID_EFECTO_SECUNDARIO)
